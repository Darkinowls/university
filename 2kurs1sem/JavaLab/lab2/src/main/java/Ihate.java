public class Ihate {
    public static int[] IntArray(int j , int[] a){
        int[] b = new int[j] ;

        for (int i = 0; i < j ; i++ ){
            b[i] = a[i] ;
        }
        return b ;
    }

    public static char[] CharArray(int j , char[] a){
        char[] b = new char[j] ;

        for (int i = 0; i < j ; i++ ){
            b[i] = a[i] ;
        }
        return b ;
    }
}
